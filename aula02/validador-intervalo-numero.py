#Este código verifica se um número está dentro de um intervalo específico (entre 10 e 20).

numero = int(input("Digite um número: ")) #Solicita um número ao usuário.

if numero >= 10 and numero <= 20: # Verifica se o número está entre 10 e 20, se estiver, exibe que o número está entre 10 e 20.
    print("O número está entre 10 e 20")
else: #Se não estiver, exibe que o número não está entre 10 e 20.
    print("O número não está entre 10 e 20")
