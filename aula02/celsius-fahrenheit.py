#Este código converte uma temperatura de Celsius para Fahrenheit.

celsius = float(input("Digite a temperatura em Celsius: ")) #Lê a temperatura em Celsius.

fahrenheit = (celsius * 9/5) + 32 #Converte Celsius para Fahrenheit usando a fórmula (C * 9/5) + 32.

print(f"{celsius}ºC equivale a {fahrenheit}ºF") #Exibe a temperatura convertida.
