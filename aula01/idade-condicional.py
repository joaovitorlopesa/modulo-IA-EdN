#Este código verifica se a idade digitada é maior ou igual a 18.

idade = 15 #Definindo a variável idade com o valor 15.

if idade >= 18: #Se a idade for maior ou igual a 18, exibe "Você é maior de idade.".
    print("Você é maior de idade.") 
else: #Se não for, exibe "Você é menor de idade.".
    print("Você é menor de idade.") 
#Fim do código.