#Este código calcula o preço final de um celular com desconto.

preco = 1000 #Preço original do celular.
desconto = 15 #Desconto em porcentagem.

valor_final = preco - (preco * desconto / 100) #Calculo do valor final com desconto.
print(f"O preço do celular de Maria com {desconto}% de desconto é R$ {valor_final}") #Exibe o valor final.
